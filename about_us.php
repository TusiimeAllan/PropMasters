        <h1>About Prop Masters</h1>
        <p>The property management application, developed as part of an assignment for the Internet Programming course, aims to provide a comprehensive solution
            for property management in the real estate industry. The application offers a user-friendly platform where individuals can register and access a range
            of features related to property management. Upon registration, users gain the ability to log in and utilize the application in different roles. For
            landlords, the system allows them to create and add properties, while potential tenants can browse and view available properties. The application
            focuses on streamlining property-related operations and ensuring a seamless experience for both landlords and tenants.</p>

        <br />
        <p> Designed to meet the specific requirements of the assignment, the property management application offers an array of functionalities that enable
            effective property management. Landlords have the freedom to add new properties, delete existing ones, and edit property details from their
            personalized home page. Property information is meticulously captured, including details such as property name, type, location, description,
            number of bedrooms and bathrooms, maps, cost, and photos. To maintain quality control, all properties created by landlords undergo an approval
            process overseen by an administrator. Only after approval, the properties are displayed on the main page for potential tenants to explore.</p>

